function popup() {
  var userChoice = prompt("Security level of password (1 = all lowercase, 2 = mixed upper and lower case, 3 = added numbers, 4 = added special characters")
  
    
  if 
    (userChoice === "1"){
    document.getElementById("password").innerHTML = pass1(10)
  }else if
    (userChoice === "2"){
    document.getElementById("password").innerHTML = pass2(10)
  }else if
    (userChoice === "3"){
    document.getElementById("password").innerHTML = pass3(10)
  }else{
    document.getElementById("password").innerHTML = pass4(10)
  }
}

document.getElementById("generate").addEventListener("click", popup);
